import java.util.Scanner;

public class SquareOfNumber {
    public static void main() {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the value of number: ");
        int n = sc.nextInt();
        System.out.print("The Sqaure of " + n + " is: " + (n*n));
    }
}
